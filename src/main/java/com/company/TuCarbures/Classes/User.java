package com.company.TuCarbures.Classes;

public class User {
    public String idUser;
    public String userName;
    public String favoriteStation;
    public String adress;
    public int zipCode;
    public String city;
    public int numberPhone;

    public User(String idUser, String userName, String favoriteStation, String adress, int zipCode, String city, int numberPhone) {

        this.idUser = idUser;
        this.userName = userName;
        this.favoriteStation = favoriteStation;
        this.adress = adress;
        this.zipCode = zipCode;
        this.city = city;
        this.numberPhone = numberPhone;

    }
}

